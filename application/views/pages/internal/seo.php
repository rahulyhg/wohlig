<h3>SEO</h3>
<p>Search engine optimization is a methodology of strategies, techniques and tactics used to increase the amount of visitors to a website by obtaining a high-ranking placement in the search results page of a search engine (SERP) -- including Google, Bing, Yahoo and other search engines.</p>
<p>SEO helps to ensure that a site is accessible to a search engine and improves the chances that the site will be found by the search engine.</p>
<p>It is common practice for Internet users to not click through pages and pages of search results, so where a site ranks in a search is essential for directing more traffic toward the site. The higher a website naturally ranks in organic results of a search, the greater the chance that that site will be visited by a user.</p>
<p>Types:</p>
<ul>
  <li>Pay Per Click</li>
  <li>Social Media Optimisation (SMO)</li>
  <li>Analytics</li>
  <li>Google Ad Words</li>
</ul>
